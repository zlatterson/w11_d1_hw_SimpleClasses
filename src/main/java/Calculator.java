public class Calculator {
    public int add(int number1, int number2){
        return number1 + number2;
    }

    public int subtract(int i, int i1) {
        return i - i1;
    }

    public int multiply(int i, int i1) {
        return i * i1;
    }

    public double divide(double i, double i1) {
        return i / i1;
    }
}
